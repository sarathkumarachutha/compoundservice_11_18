DROP TABLE IF EXISTS CUSTOMER_SOS;
 
CREATE TABLE CUSTOMER_SOS (
  cust_sos_id int(20) AUTO_INCREMENT  PRIMARY KEY,
  cust_id int(20) NOT NULL,
  cust_email VARCHAR(100) NOT NULL,
  cust_first_name VARCHAR(100) NOT NULL,
  cust_last_name VARCHAR(100) NOT NULL
);

	
DROP TABLE IF EXISTS SALES_ORDER;
CREATE TABLE SALES_ORDER (
  ORDER_ID LONG(20) AUTO_INCREMENT  PRIMARY KEY,
  order_date VARCHAR(30) NOT NULL,
  cust_id int(20) NOT NULL,
  order_desc VARCHAR(500) NOT NULL,
  total_price LONG(20) NOT NULL,
  
);

DROP TABLE IF EXISTS ORDER_LINE_ITEM;
CREATE TABLE ORDER_LINE_ITEM (
  ORDER_LINE_ID LONG(20) AUTO_INCREMENT  PRIMARY KEY,
  item_name VARCHAR(200) NOT NULL,
  item_quantity LONG(20) NOT NULL,
  order_id LONG(20) NOT NULL,
);
